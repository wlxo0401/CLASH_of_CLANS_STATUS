package user;

public class User {
	String userName;
	int plunderGold;
	int plunderElixir;
	int plunderDarkElixir;
	public String getUserName() {
		return userName;
	}
	public void setUserName(String userName) {
		this.userName = userName;
	}
	public int getPlunderGold() {
		return plunderGold;
	}
	public void setPlunderGold(int plunderGold) {
		this.plunderGold = plunderGold;
	}
	public int getPlunderElixir() {
		return plunderElixir;
	}
	public void setPlunderElixir(int plunderElixir) {
		this.plunderElixir = plunderElixir;
	}
	public int getPlunderDarkElixir() {
		return plunderDarkElixir;
	}
	public void setPlunderDarkElixir(int plunderDarkElixir) {
		this.plunderDarkElixir = plunderDarkElixir;
	}
}
